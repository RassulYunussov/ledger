func main() {

	addr := "localhost:50051"
	conn, err := grpc.Dial(addr, grpc.WithTransportCredentials(insecure.NewCredentials()))
	if err != nil {
		log.Fatal(err)
	}
	defer conn.Close()
	client := transfer.NewTransferClient(conn)
	md := metadata.Pairs("subsystem", "9667453c-e685-4c2a-a1ba-98f2efda9b0a")
	ctx := metadata.NewOutgoingContext(context.TODO(), md)

	seconds := flag.Int("seconds", 10, "seconds")
	requests := flag.Int("requests", 5, "rps")
	flag.Parse()

	wg := sync.WaitGroup{}
	for j := 0; j < *seconds; j++ {
		for i := 0; i < *requests; i++ {
			company := companies[rand.Intn(len(companies))]
			kaspi := kaspis[rand.Intn(len(kaspis))]
			card := cards[rand.Intn(len(cards))]
			wg.Add(2)
			// from kaspi to card
			go func(from uuid.UUID, to uuid.UUID) {
				id := uuid.New()
				sourceUuid := from
				targetUuid := to
				transaction, err := client.CreateTransaction(ctx, &transfer.TransactionRequest{
					Id:          id[:],
					Source:      sourceUuid[:],
					Target:      targetUuid[:],
					Amount:      1,
					Fractional:  0,
					Currency:    "KZT",
					Description: "deposit",
				})
				if err != nil {
					log.Println(err)
				}
				log.Println(transaction)
				wg.Done()
			}(kaspi, card)
			// from card to company
			go func(from uuid.UUID, to uuid.UUID) {
				id := uuid.New()
				sourceUuid := from
				targetUuid := to
				transaction, err := client.CreateTransaction(ctx, &transfer.TransactionRequest{
					Id:          id[:],
					Source:      sourceUuid[:],
					Target:      targetUuid[:],
					Amount:      1,
					Currency:    "KZT",
					Description: "gift",
				})
				if err != nil {
					log.Println(err)
				}
				log.Println(transaction)
				wg.Done()
			}(card, company)
		}
		time.Sleep(time.Second)
	}
	wg.Wait()
}
